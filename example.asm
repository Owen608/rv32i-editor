 ============================================================
 测试汇编示例：带伪指令 / 不带伪指令（可手动模拟）
 支持的伪指令：nop, mv, li, ret
 ============================================================

 ------------------------------------------------------------
 示例 1：带伪指令 — a0=1+2，然后 exit(93)
 手动模拟：a0: 1 -> 3, a1: 2, a7: 93, 最后 ecall 结束
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   nop
   li   a0, 1
   li   a1, 2
   add  a0, a0, a1      ; a0 = 3
   li   a7, 93
   ecall

 ------------------------------------------------------------
 示例 2：不带伪指令 — 与示例 1 等价（仅基指令）
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   addi x0, x0, 0       ; nop
   addi a0, x0, 1
   addi a1, x0, 2
   add  a0, a0, a1
   addi a7, x0, 93
   ecall

 ============================================================
 示例 3：带伪指令 — 使用 mv，a0 = 10, a1 = 20, a0 = a0 + a1 = 30
 ============================================================
 .text
 .globl _start
 _start:
   li   t0, 10
   li   t1, 20
   mv   a0, t0          ; a0 = 10
   mv   a1, t1          ; a1 = 20
   add  a0, a0, a1      ; a0 = 30
   li   a7, 93
   ecall

 ------------------------------------------------------------
 示例 4：不带伪指令 — 与示例 3 等价
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   addi t0, x0, 10
   addi t1, x0, 20
   addi a0, t0, 0       ; mv a0, t0
   addi a1, t1, 0       ; mv a1, t1
   add  a0, a0, a1
   addi a7, x0, 93
   ecall

 ============================================================
 示例 5：带伪指令 + 分支（标签）— 若 a0==a1 则跳到 same，否则 a0=1 后退出
 手动模拟：a0=2, a1=2 -> beq 成立 -> same: a0=99 -> exit
 ============================================================
 .text
 .globl _start
 _start:
   li   a0, 2
   li   a1, 2
   beq  a0, a1, same   ; 相等则跳
   li   a0, 1
   li   a7, 93
   ecall
 same:
   li   a0, 99
   li   a7, 93
   ecall

 ------------------------------------------------------------
 示例 6：不带伪指令 — 与示例 5 等价（分支目标用数字偏移也可，这里用标签）
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   addi a0, x0, 2
   addi a1, x0, 2
   beq  a0, a1, same
   addi a0, x0, 1
   addi a7, x0, 93
   ecall
 same:
   addi a0, x0, 99
   addi a7, x0, 93
   ecall

 ============================================================
 示例 7：带伪指令 — ret（jalr x0, ra, 0）：子程序返回
 func 里 li a0,42 再 ret；_start 用 jal ra,func 调用，得到 a0=42 后 exit
 手动模拟：jal -> func -> a0=42 -> ret -> 回到 _start 下一条 -> ecall
 ============================================================
 .text
 .globl _start
 _start:
   jal  ra, func        ; 调用 func，返回地址存 ra
   li   a7, 93
   ecall
 func:
   li   a0, 42
   ret                   ; jalr x0, ra, 0
   nop

 ------------------------------------------------------------
 示例 8：不带伪指令 — 与示例 7 等价
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   jal  ra, func
   addi a7, x0, 93
   ecall
 func:
   addi a0, x0, 42
   jalr x0, ra, 0        ; ret
   addi x0, x0, 0       ; nop

 ============================================================
 示例 9：带伪指令 — 大立即数 li（展开为 lui + addi）
 a0 = 0x12345（超出 12 位），需两条指令
 ============================================================
 .text
 .globl _start
 _start:
   li   a0, 0x12345     ; lui + addi
   li   a7, 93
   ecall

 ------------------------------------------------------------
 示例 10：不带伪指令 — 与示例 9 等价（手写 lui + addi）
 0x12345: hi=0x12, lo=0x345
 ------------------------------------------------------------
 .text
 .globl _start
 _start:
   lui  a0, 0x12
   addi a0, a0, 0x345
   addi a7, x0, 93
   ecall

 ============================================================
 以下为可直接复制到编辑器运行的“未注释”版本（任选其一）
 ============================================================

 ---------- 复制块 A：带伪指令，a0=1+2 后 exit ----------
.text
.globl _start
_start:
  nop
  li   a0, 1
  li   a1, 2
  add  a0, a0, a1
  li   a7, 93
  ecall

 ---------- 复制块 B：不带伪指令，与 A 等价 ----------
 .text
 .globl _start
 _start:
   addi x0, x0, 0
   addi a0, x0, 1
   addi a1, x0, 2
   add  a0, a0, a1
   addi a7, x0, 93
   ecall

 ---------- 复制块 C：带伪指令，mv + 分支 + ret ----------
 .text
 .globl _start
 _start:
   li   a0, 2
   li   a1, 2
   beq  a0, a1, same
   li   a0, 1
   li   a7, 93
   ecall
 same:
   li   a0, 99
   li   a7, 93
   ecall

 ---------- 复制块 D：带 ret 的子程序调用 ----------
 .text
 .globl _start
 _start:
   jal  ra, func
   li   a7, 93
   ecall
 func:
   li   a0, 42
   ret

 ---------- 复制块 E：load/store（指令区从 0x0，数据区从 0x80000000）----------
 ; 数据区基址 0x80000000，用 lui 形成地址；sw 存字，lw 取字
 .text
 .globl _start
 _start:
   lui  t0, 0x80000       ; t0 = 0x80000000（数据区基址）
   addi a0, x0, 0x123     ; a0 = 0x123，待写入的值
   sw   a0, 0(t0)        ; 存字到 0x80000000
   lw   a1, 0(t0)        ; 从 0x80000000 取字到 a1（a1 应为 0x123）
   addi a7, x0, 93
   ecall

 ---------- 复制块 F：sb/lb 字节存取（指令 0x0，数据区 0x80000000）----------
 .text
 .globl _start
 _start:
   lui  t0, 0x80000     ; t0 = 0x80000000
   addi a0, x0, 0xAB    ; a0 = 0xAB
   sb   a0, 0(t0)      ; 存字节到 0x80000000
   lb   a1, 0(t0)      ; 取字节（有符号）到 a1
   lbu  a2, 0(t0)      ; 取字节（无符号）到 a2
   addi a7, x0, 93
   ecall

 ---------- for 循环：for(int i=1; i<=n; i++) a0+=2; ----------
 ; n 放在 a1，循环前 a0=0；结束时 a0 = 2*n（n>=1 时）
 .text
 .globl _start
 _start:
   li   a0, 0          ; a0 = 0（累加结果）
   li   a1, 5          ; n = 5（可改）
   li   t0, 1          ; i = 1
 loop:
   blt  a1, t0, end_loop   ; if n < i (即 i > n) 跳出
   addi a0, a0, 2      ; a0 += 2
   addi t0, t0, 1      ; i++
   j    loop
 end_loop:
   li   a7, 93
   ecall
